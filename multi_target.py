def calculate_min_path(lst_end, grid):
    lst_point = lst_end[1:]
    perms = permutations(lst_point)
    min_distance = float('inf')
    min_path = None

    for path in perms:
        total_distance = dastar(grid, lst_end[0], path[0])
        for i in range(len(path) - 1):
            total_distance += dastar(grid, path[i], path[i + 1])
            if i == len(path) - 1:
                total_distance = total_distance + dastar(grid, path[i], lst_end[0])
        if total_distance < min_distance:
            min_distance = total_distance
            min_path = path

    return [lst_end[0]] + list(min_path) + [lst_end[0]]


def calculate_min_path2(lst_end, grid):
    lst_point = lst_end[1:]
    n = len(lst_point)
    dp = [[float('inf')] * n for _ in range(n)]

    for i in range(n):
        dp[i][0] = dastar(grid, lst_end[0], lst_point[i])

    for j in range(1, n):
        for i in range(n):
            for k in range(n):
                if k != i:
                    dp[i][j] = min(dp[i][j], dp[k][j - 1] + dastar(grid, lst_point[k], lst_point[i]))

    min_distance = float('inf')
    min_path = None
    for i in range(n):
        total_distance = dp[i][n - 1] + dastar(grid, lst_point[i], lst_end[0])
        if total_distance < min_distance:
            min_distance = total_distance
            min_path = [lst_end[0], lst_point[i]]
            j = n - 1
            while j > 0:
                for k in range(n):
                    if k != i and dp[k][j - 1] + dastar(grid, lst_point[k], lst_point[i]) == dp[i][j]:
                        min_path.append(lst_point[k])
                        i = k
                        j -= 1
                        break
            min_path += lst_point[::-1]

    return min_path + [lst_end[0]]


def make_grid(rows, width):
    grid = []
    gap = width // rows
    for i in range(rows):
        grid.append([])
        for j in range(rows):
            spot = Spot(i, j, gap, rows)
            grid[i].append(spot)

    return grid


def draw_grid(win, rows, width):
    gap = width // rows
    for i in range(rows):
        pygame.draw.line(win, GREY, (0, i * gap), (width, i * gap))
        for j in range(rows):
            pygame.draw.line(win, GREY, (j * gap, 0), (j * gap, width))


def draw(win, grid, rows, width):
    win.fill(WHITE)
    for row in grid:
        for spot in row:
            spot.draw(win)

    draw_grid(win, rows, width)
    pygame.display.update()


def get_clicked_pos(pos, rows, width):
    gap = width // rows
